// Assignment Operator = vs Equality Operator == vs Strict Equality Operator ===
/*
	Assignment operator is used to assign values. Equality operator is used to check if two
	variables have the same value irrespective of their types. Strict equality operator
	is used to compare if two variables have the same value AND the same type.
*/


